const { test, expect } = require("@jest/globals");
const { charBox } = require("./011-makingBox2");

test("charbox array ", () => {
  expect(charBox(10)).toEqual([
    ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
    ["#", " ", " ", " ", " ", " ", " ", " ", " ", "#"],
    ["#", " ", " ", " ", " ", " ", " ", " ", " ", "#"],
    ["#", " ", " ", " ", " ", " ", " ", " ", " ", "#"],
    ["#", " ", " ", " ", " ", " ", " ", " ", " ", "#"],
    ["#", " ", " ", " ", " ", " ", " ", " ", " ", "#"],
    ["#", " ", " ", " ", " ", " ", " ", " ", " ", "#"],
    ["#", " ", " ", " ", " ", " ", " ", " ", " ", "#"],
    ["#", " ", " ", " ", " ", " ", " ", " ", " ", "#"],
    ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
  ]);
  expect(charBox(3)).toEqual([
    ["#", "#", "#"],
    ["#", " ", "#"],
    ["#", "#", "#"],
  ]);
  expect(charBox(1)).toEqual([["#"]]);
  expect(charBox(0)).toEqual([[]]);
});
test("special cases", () => {
  expect(charBox("Hi")).toBe(-1);
  expect(charBox(-23)).toBe(-1);
  expect(charBox(0.3)).toBe(-1);
});
